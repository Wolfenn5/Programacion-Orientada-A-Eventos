    private void llenarTabla(){
    DefaultTableModel modelo = new DefaultTableModel(){
        @Override
        public boolean isCellEditable(int fila, int columna) {
        int ultimaColumna = getColumnCount()-1;
        if (columna == 0) {
            return false;
        }
        else if (columna == ultimaColumna) {
            return false;
        } return true;
    } };
    
    tblDatos.setModel(modelo);

    LlenarTabla llenar = new LlenarTabla();

    Vector<String> titulos = llenar.titulosColumnas();
    for(String t: titulos){
        modelo.addColumn(t);
    }

    Vector filas = llenar.llenarFilas();
    modelo.setDataVector(filas, titulos);
}


