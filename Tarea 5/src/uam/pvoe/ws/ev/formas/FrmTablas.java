    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        
int nFilas = tblDatos.getRowCount();
    int nCols  = tblDatos.getColumnCount();

    for(int i=0;i<nFilas;i++){
        int suma = 0;

        for(int j=1;j<nCols-1;j++){
            String valor = (String)tblDatos.getValueAt(i, j);
            int cantidad = 0;

            try{
                cantidad = Integer.parseInt(valor);
            }catch(Exception e){

                JOptionPane.showMessageDialog(this,
                    "El valor \"" + valor + "\" no es válido.\n"
                    + "Inserte un valor entero o se reemplazará automáticamente por 0.",
                    "Dato inválido",
                    JOptionPane.WARNING_MESSAGE
                );

                cantidad = 0;
                tblDatos.setValueAt("0", i, j);   
            }

            suma += cantidad;
        }

        int faltante = 10 - suma;
        if(faltante < 0) faltante = 0;

        tblDatos.setValueAt("" + faltante, i, nCols-1);
    }
    }//GEN-LAST:event_btnCalcularActionPerformed

